func fileExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && !info.IsDir()
}

func hasFiles(dir string, exts ...string) bool {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false
	}
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		for _, ext := range exts {
			if strings.HasSuffix(e.Name(), ext) {
				return true
			}
		}
	}
	return false
}

func hasSubdirFiles(parentDir, filename string) bool {
	entries, err := os.ReadDir(parentDir)
	if err != nil {
		return false
	}
	for _, e := range entries {
		if !e.IsDir() {
			continue
		}
		if fileExists(filepath.Join(parentDir, e.Name(), filename)) {
			return true
		}
	}
	return false
}

// isIgnoredDir checks if a directory name should be excluded from ecosystem scans.
// This prevents spurious detection of manifests in build artifacts, caches,
// and singletons like .devcontainer that are handled elsewhere.
func isIgnoredDir(name string) bool {
	switch name {
	case
		// Category A – handled exclusively by root singletons
		".devcontainer", ".github", ".gitlab", ".circleci", ".husky",
		// VCS
		".git", ".svn", ".hg", ".bzr",
		// IDE / Editor
		".idea", ".vscode", ".fleet",
		// JavaScript / Node.js
		"node_modules", "bower_components", "jspm_packages",
		// Dependency mirrors / ecosystems
		"vendor", "Pods",
		// Infrastructure as Code
		".terraform", ".terragrunt-cache", ".serverless", ".pulumi",
		// Build outputs (language-agnostic / generic)
		"dist", "build", "out", "target", "_build", ".build", "bin", "obj",
		// Java / JVM
		".gradle", ".m2", ".mvn",
		// C / C++
		"cmake-build-debug", "cmake-build-release", "CMakeFiles",
		// Framework build outputs
		".next", ".nuxt", ".output", ".svelte-kit", ".astro", ".vuepress", ".vitepress", ".docusaurus",
		// Bundler / Monorepo / Deployment caches
		".parcel-cache", ".turbo", ".vercel", ".netlify", ".webpack", ".nx", ".expo",
		// Python virtual environments
		".venv", "venv", "virtualenv", "env", ".env",
		// Python build / cache artifacts
		"__pycache__", ".eggs", ".tox", ".mypy_cache", ".ruff_cache", ".pytest_cache", ".hypothesis",
		// Other Language-specific caches
		"elm-stuff", ".bundle", ".stack-work", "storybook-static", ".cabal-sandbox",
		// Test coverage artifacts
		"coverage", ".nyc_output",
		// General caches and temp directories
		".cache", ".tmp", "tmp", "temp",
		// Test data and mocks
		"testdata", "fixtures", "mocks", "__mocks__":
		return true
	}
	return false
}

// gitTrackedFiles returns a list of files tracked by git in the current directory.
// Returns an error if git is not available or the current directory is not a git repo.
func gitTrackedFiles() ([]string, error) {
	cmd := exec.Command("git", "ls-files")
	var out bytes.Buffer
	cmd.Stdout = &out
	if err := cmd.Run(); err != nil {
		return nil, err
	}

	var paths []string
	scanner := bufio.NewScanner(&out)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line != "" {
			paths = append(paths, line)
		}
	}
	return paths, scanner.Err()
}

// classifyFile inspects a file path and emits a dependabot ecosystem entry
// if it matches a known manifest pattern.
func classifyFile(path string, emit func(ecosystem, directory string)) {
	name := filepath.Base(path)
	dir := filepath.Dir(path)
	ext := filepath.Ext(name)

	switch name {
	case "package.json":
		emit("npm", dir)
	case "requirements.txt", "requirements-dev.txt", "setup.py", "setup.cfg", "Pipfile":
		emit("pip", dir)
	case "go.mod":
		emit("gomod", dir)
	case "Cargo.toml":
		emit("cargo", dir)
	case "composer.json":
		emit("composer", dir)
	case "Gemfile":
		emit("bundler", dir)
	case "Dockerfile", "docker-compose.yml", "docker-compose.yaml":
		emit("docker", dir)
	case "mix.exs":
		emit("mix", dir)
	case "elm.json":
		emit("elm", dir)
	case "build.gradle", "build.gradle.kts":
		emit("gradle", dir)
	case "pom.xml":
		emit("maven", dir)
	case "packages.config":
		emit("nuget", dir)
	case "pubspec.yaml":
		emit("pub", dir)
	case "Package.swift":
		emit("swift", dir)
	case "WORKSPACE", "MODULE.bazel":
		emit("bazel", dir)
	case "bun.lockb", "bunfig.toml":
		emit("bun", dir)
	case "environment.yml", "environment.yaml":
		emit("conda", dir)
	case "Chart.yaml":
		emit("helm", dir)
	case "Project.toml":
		emit("julia", dir)
	case "uv.lock":
		emit("uv", dir)
	case "vcpkg.json":
		emit("vcpkg", dir)
	case "global.json":
		emit("dotnet-sdk", dir)
	}

	switch ext {
	case ".tf":
		emit("terraform", dir)
	case ".csproj", ".fsproj":
		emit("nuget", dir)
	}

	if strings.HasPrefix(name, "Dockerfile.") {
		emit("docker", dir)
	}
}
