var (
	depTargetBranch   string
	depPrLimit        int
	depCooldownDays   int
	depUpdateInterval string
	depUpdateDay      string
	depDryRun         bool
)

var generateDependabotCmd = &cobra.Command{
	Use:   "dependabot",
	Short: "Auto-generate dependabot.yml from detected ecosystems",
	Args:  cobra.NoArgs,
	RunE:  runGenerateDependabot,
}

func init() {
	generateDependabotCmd.Flags().StringVarP(&generateOutput, "output", "o", "", "write to file instead of stdout")
	generateDependabotCmd.Flags().StringVar(&depTargetBranch, "target-branch", "dev", "Target branch for PRs")
	generateDependabotCmd.Flags().IntVar(&depPrLimit, "pr-limit", 5, "Max open PRs per ecosystem")
	generateDependabotCmd.Flags().IntVar(&depCooldownDays, "cooldown", 7, "Days to wait before updating")
	generateDependabotCmd.Flags().StringVar(&depUpdateInterval, "interval", "weekly", "Update frequency")
	generateDependabotCmd.Flags().StringVar(&depUpdateDay, "day", "monday", "Day of week for updates")
	generateDependabotCmd.Flags().BoolVar(&depDryRun, "dry-run", false, "Show what would be generated without writing")

	generateCmd.AddCommand(generateDependabotCmd)
}

type DepEntry struct {
	Ecosystem    string
	Directory    string
	TargetBranch string
	PrLimit      int
	Interval     string
	Day          string
	Cooldown     int
	Prefix       string
	Labels       []string
}

func runGenerateDependabot(cmd *cobra.Command, args []string) error {
	formatter := output.NewFormatter(output.FormatterOptions{
		Format:  getOutputFormat(),
		NoColor: false,
		Writer:  os.Stdout,
		Quiet:   quiet,
	})

	entries := scanForEcosystems()

	if len(entries) == 0 {
		formatter.Warning("No ecosystems detected. Ensure manifest files exist.", nil)
	}

	tmplStr := `---
# Dependabot Version Updates (Auto-generated by unirtm generate dependabot)
#
# ⚠️  DO NOT EDIT MANUALLY — This file is regenerated automatically.
# To update, run: unirtm generate dependabot --output .github/dependabot.yml
#
# Purpose: Automatically maintains project dependencies to ensure security and stability.

version: 2
updates:{{range .}}
  - package-ecosystem: "{{.Ecosystem}}"
    directory: "{{.Directory}}"
    target-branch: "{{.TargetBranch}}"
    open-pull-requests-limit: {{.PrLimit}}
    rebase-strategy: "auto"
    groups:
      all-dependencies:
        patterns: ["*"]
        update-types:
          - "patch"
          - "minor"
          - "major"
    schedule:
      interval: "{{.Interval}}"
      day: "{{.Day}}"
    cooldown:
      default-days: {{.Cooldown}}
    commit-message:
      prefix: "{{.Prefix}}"
    labels:{{range .Labels}}
      - "{{.}}"{{end}}
{{end}}`

	tmpl, err := template.New("dependabot").Parse(tmplStr)
	if err != nil {
		return err
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, entries); err != nil {
		return err
	}

	if depDryRun {
		fmt.Println("# [DRY-RUN] Would write to dependabot.yml:")
		fmt.Println(buf.String())
		return nil
	}

	if generateOutput == "" {
		fmt.Print(buf.String())
		return nil
	}

	dir := filepath.Dir(generateOutput)
	if dir != "" && dir != "." {
		if err := os.MkdirAll(dir, 0755); err != nil {
			return err
		}
	}

	if err := os.WriteFile(generateOutput, buf.Bytes(), 0644); err != nil {
		formatter.Error(fmt.Sprintf("Failed to write %s: %v", generateOutput, err))
		return err
	}

	formatter.Success(fmt.Sprintf("Generated %s successfully with %d ecosystems detected.", generateOutput, len(entries)), nil)
	return nil
}

func scanForEcosystems() []DepEntry {
	emitted := make(map[string]bool)
	var entries []DepEntry

	emit := func(ecosystem, directory string) {
		// Clean directory for dependabot format (must start with /)
		directory = filepath.ToSlash(directory)
		if directory == "." || directory == "" {
			directory = "/"
		} else if !strings.HasPrefix(directory, "/") {
			directory = "/" + directory
		}

		key := ecosystem + ":" + directory
		if emitted[key] {
			return
		}
		emitted[key] = true

		prefix := "chore(deps):"
		label := getEcosystemLabel(ecosystem)
		labels := []string{"dependencies", label}

		switch ecosystem {
		case "github-actions":
			prefix = "ci(deps):"
			labels = append(labels, "devops")
		case "docker", "devcontainers":
			prefix = "build(deps):"
			labels = append(labels, "infrastructure")
		case "pre-commit":
			labels = append(labels, "linting")
		}

		entries = append(entries, DepEntry{
			Ecosystem:    ecosystem,
			Directory:    directory,
			TargetBranch: depTargetBranch,
			PrLimit:      depPrLimit,
			Interval:     depUpdateInterval,
			Day:          depUpdateDay,
			Cooldown:     depCooldownDays,
			Prefix:       prefix,
			Labels:       labels,
		})
	}

	// 1. Check Root Singletons
	if hasFiles(".github/workflows", ".yml", ".yaml") {
		emit("github-actions", "/")
	}
	if fileExists(".gitmodules") {
		emit("gitsubmodule", "/")
	}
	if fileExists(".pre-commit-config.yaml") {
		emit("pre-commit", "/")
	}
	if fileExists("rust-toolchain") || fileExists("rust-toolchain.toml") {
		emit("rust-toolchain", "/")
	}
	if fileExists(".devcontainer.json") || fileExists(".devcontainer/devcontainer.json") || hasSubdirFiles(".devcontainer", "devcontainer.json") {
		emit("devcontainers", "/")
	}

	// 2. Enumerate source files and classify ecosystems.
	//
	// Strategy: prefer "git ls-files" when inside a git repository because it
	// automatically respects .gitignore, excluding build artifacts, caches,
	// virtual environments, and any other generated directories — no static
	// skip-list required.  Fall back to filepath.WalkDir with a curated skip
	// list when git is unavailable (e.g. a freshly unpacked tarball).
	if paths, err := gitTrackedFiles(); err == nil {
		// Git-aware path: only tracked (committed + staged) files are visited.
		for _, path := range paths {
			// Even if tracked, we must ignore Category A (handled by singletons)
			// and accidentally committed vendor/ build outputs.
			ignored := false
			for _, part := range strings.Split(filepath.ToSlash(path), "/") {
				if isIgnoredDir(part) {
					ignored = true
					break
				}
			}
			if !ignored {
				classifyFile(path, emit)
			}
		}
	} else {
		// Fallback: filesystem walk with a best-effort skip list.
		_ = filepath.WalkDir(".", func(path string, d fs.DirEntry, err error) error {
			if err != nil {
				return nil
			}
			if d.IsDir() {
				if isIgnoredDir(d.Name()) {
					return filepath.SkipDir
				}
				return nil
			}
			classifyFile(path, emit)
			return nil
		})
	}

	// Sort entries for deterministic output
	sort.Slice(entries, func(i, j int) bool {
		if entries[i].Ecosystem == entries[j].Ecosystem {
			return entries[i].Directory < entries[j].Directory
		}
		return entries[i].Ecosystem < entries[j].Ecosystem
	})

	return entries
}

